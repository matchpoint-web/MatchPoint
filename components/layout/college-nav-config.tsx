export function getCollegePageTitle(pathname: string): string {
  if (pathname === "/college/dashboard" || pathname === "/college") {
    return "Dashboard";
  }

  if (pathname.startsWith("/college/players/")) {
    return "Player Profile";
  }

  const match = collegeNavItems
    .filter((item) => item.href !== "/college/dashboard")
    .find(
      (item) =>
        pathname === item.href || pathname.startsWith(`${item.href}/`),
    );

  return match?.label ?? "Dashboard";
}

export function isCollegeNavItemActive(pathname: string, href: string): boolean {
  if (href === "/college/dashboard") {
    return pathname === "/college/dashboard" || pathname === "/college";
  }
  if (href === "/college/players") {
    return (
      pathname === "/college/players" ||
      pathname.startsWith("/college/players/")
    );
  }
  return pathname === href || pathname.startsWith(`${href}/`);
}
